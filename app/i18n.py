def normalize_language(value: str | None) -> str:
    candidate = (value or "").strip().lower()
    return candidate if candidate in SUPPORTED_LANGUAGES else DEFAULT_LANGUAGE


def normalize_theme_preference(value: str | None) -> str:
    candidate = (value or "").strip().lower()
    return candidate if candidate in SUPPORTED_THEME_PREFERENCES else "dark"


def translate(language: str, key: str, **kwargs: object) -> str:
    normalized_language = normalize_language(language)
    template = (
        TRANSLATIONS.get(normalized_language, {}).get(key)
        or TRANSLATIONS[DEFAULT_LANGUAGE].get(key)
        or key
    )
    if kwargs:
        return template.format(**kwargs)
    return template


def language_options() -> list[tuple[str, str]]:
    return [(code, translate(code, f"language.{code}")) for code in SUPPORTED_LANGUAGES]


def theme_options(language: str) -> list[tuple[str, str]]:
    return [(code, translate(language, f"theme.{code}")) for code in SUPPORTED_THEME_PREFERENCES]


